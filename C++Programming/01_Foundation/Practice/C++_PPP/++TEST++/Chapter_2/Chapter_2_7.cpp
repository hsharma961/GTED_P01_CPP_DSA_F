#include <iostream>
#include <string>
using namespace std;

int main()
{
	
	cout << "Enter a spell_number between zero - four (0-4) : ";

	string spell_num;
	
	cin >> spell_num;
	
	if(spell_num == "zero"|| spell_num == "Zero" || spell_num == "ZERO"){
		cout << "this is 0 \n";
	}
	else if(spell_num == "one"||spell_num == "One" || spell_num == "ONE"){
		cout << "this is 1 \n";
	}
	else if(spell_num == "two"|| spell_num == "Two" || spell_num == "TWO"){
		cout << "this is 2 \n";
	}
	else if(spell_num == "three"|| spell_num == "Three" || spell_num == "THREE"){
		cout << "this is 3 \n";
	}
	else if(spell_num == "four"|| spell_num == "Four" || spell_num == "FOUR"){
		cout << "this is 4 \n";
	}
	else { 
		cout << "not a number I know \n";
	}

	system("pause");
	return 0;
}

